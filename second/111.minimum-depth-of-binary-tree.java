/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public int minDepth(TreeNode root) {
        return afterTravel(root);
    }

    public static int afterTravel(TreeNode node) {
        if (node == null) return 0;
        int leftNum = afterTravel(node.left);
        int rightNum = afterTravel(node.right);
        if (node.left == null) 
            return 1 + rightNum;
        else if (node.right == null)
            return 1 + leftNum;
        else {
            return 1 + Math.min(leftNum,rightNum);
        }

    }
}

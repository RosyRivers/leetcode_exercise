/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public int rob(TreeNode root) {
        // dp[0]表示不偷当前节点，dp[1]表示偷当前节点
        int[] dp = new int[2];
        dp = behindTravel(root);
        return Math.max(dp[0], dp[1]);

    }

    public int[] behindTravel(TreeNode node) {
        if (node == null) return new int[] {0, 0};
        int[] left, right;
        left = behindTravel(node.left);
        right = behindTravel(node.right);
        // 偷当前节点
        int tou = node.val + left[0] + right[0];
        // 不偷当前节点, 则判断是否偷左右子节点，目标是让当前节点值最大
        int notou = Math.max(left[0], left[1]) + Math.max(right[0], right[1]);
        return new int[] {notou, tou};
    }
}
